package projetoprolog;

/**
 *
 * @author pc
 */
public class Partida {
    
    private String data;
    private int pontJog;
    private int pontComp;
    private char vez;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getPontJog() {
        return pontJog;
    }

    public void setPontJog(int pontJog) {
        this.pontJog = pontJog;
    }

    public int getPontComp() {
        return pontComp;
    }

    public void setPontComp(int pontComp) {
        this.pontComp = pontComp;
    }

    public char getVez() {
        return vez;
    }

    public void setVez(char vez) {
        this.vez = vez;
    }
    
    
    
}
